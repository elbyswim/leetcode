class Solution:
    def possibleBipartition(self, N, dislikes):
        color = {0: set(), 1: set()}
        for dl in dislikes:
            if (dl[0] in color[0] and dl[1] in color[0]) or (dl[0] in color[1] and dl[1] in color[1]):
                return False
            elif dl[0] in color[0] or dl[1] in color[1]:
                color[0].add(dl[0])
                color[1].add(dl[1])
            else:
                color[1].add(dl[0])
                color[0].add(dl[1])
        return True


